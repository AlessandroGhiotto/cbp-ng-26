#!/usr/bin/env python3
"""Plot branch predictor benchmark results.

Reads a comparison CSV file generated by benchmark_predictors.py
and generates a 2x2 grid of bar charts comparing:
- IPC (Instructions Per Cycle)
- MPI (Mispredictions Per Instruction)
- CPI (Cycles lost to misprediction penalty)
- EPI (Dynamic Energy Per Instruction)
"""

import argparse
import csv
import sys
from pathlib import Path
import matplotlib
matplotlib.use('Agg')
import matplotlib.pyplot as plt

def main() -> int:
    parser = argparse.ArgumentParser(description="Plot branch predictor benchmark results.")
    parser.add_argument("--csv", default="benchmark_results/comparison.csv", help="Path to comparison CSV file")
    parser.add_argument("--output", default="benchmark_results/comparison_plots.png", help="Path to save the output plot")
    args = parser.parse_args()

    csv_path = Path(args.csv)
    if not csv_path.exists():
        print(f"Error: CSV file not found at {csv_path}", file=sys.stderr)
        return 1

    configs = []
    ipcs = []
    cpis = []
    epis = []
    mpis = []
    dpis = []
    throughputs = []

    with open(csv_path, mode="r", newline="") as f:
        reader = csv.DictReader(f)
        for row in reader:
            configs.append(row["config"])
            ipcs.append(float(row["ipc"]))
            cpis.append(float(row["cpi"]))
            epis.append(float(row["epi"]))
            mpis.append(float(row["mpi"]))
            dpis.append(float(row["dpi"]))
            throughputs.append(float(row["throughput"]))

    if not configs:
        print("Error: No data found in CSV file.", file=sys.stderr)
        return 1

    # Set up matplotlib style for a clean, modern look
    plt.style.use("seaborn-v0_8-whitegrid" if "seaborn-v0_8-whitegrid" in plt.style.available else "default")
    fig, axs = plt.subplots(2, 3, figsize=(18, 11))
    fig.suptitle("Branch Predictor Comparison & Profiling (Harcom Metrics)", fontsize=18, fontweight="bold", y=0.98)

    # Use a modern, distinct color palette for the bars
    colors = ['#1f77b4', '#aec7e8', '#ff7f0e', '#ffbb78', '#2ca02c', '#98df8a', '#d62728', '#ff9896', '#9467bd', '#c5b0d5',
              '#8c564b', '#c49c94', '#e377c2', '#f7b6d2', '#7f7f7f', '#c7c7c7', '#bcbd22', '#dbdb8d', '#17becf', '#9edae5']

    # Map each config to a unique color to keep coloring consistent across all subplots
    config_to_color = {cfg: colors[idx % len(colors)] for idx, cfg in enumerate(configs)}

    def autolabel(rects, ax, fmt="{:.3f}"):
        """Attach a text label above each bar in *rects*, displaying its height."""
        for rect in rects:
            height = rect.get_height()
            ax.annotate(fmt.format(height),
                        xy=(rect.get_x() + rect.get_width() / 2, height),
                        xytext=(0, 3),  # 3 points vertical offset
                        textcoords="offset points",
                        ha='center', va='bottom', fontsize=8, fontweight='bold',
                        rotation=90)

    # Helper function to plot a bar chart with sorting
    def plot_metric(ax, title, ylabel, data_list, fmt, reverse_sort=False):
        # Sort data by value
        sorted_data = sorted(zip(configs, data_list), key=lambda x: x[1], reverse=reverse_sort)
        sorted_configs, sorted_vals = zip(*sorted_data)
        
        # Select colors matching sorted configurations (consistent coloring)
        bar_colors = [config_to_color[cfg] for cfg in sorted_configs]
        
        rects = ax.bar(sorted_configs, sorted_vals, color=bar_colors, edgecolor='grey', alpha=0.85)
        ax.set_title(title, fontsize=11, fontweight="bold", pad=10)
        ax.set_ylabel(ylabel, fontsize=9, fontweight="bold")
        ax.tick_params(axis='x', rotation=45, labelsize=8)
        ax.tick_params(axis='y', labelsize=8)
        autolabel(rects, ax, fmt)
        
        # Add extra padding at the top of the y-axis to ensure rotated labels are not cut off
        ymin, ymax = ax.get_ylim()
        ax.set_ylim(ymin, ymax + (ymax - ymin) * 0.20)

    # Row 1: Core Performance and Throughput
    # 1. IPC (System Throughput - Higher is better)
    plot_metric(axs[0, 0], "IPC (Instructions Per Cycle) - Higher is Better", "IPC", ipcs, "{:.3f}", reverse_sort=True)

    # 2. Prediction Throughput (npred-based - Higher is better)
    plot_metric(axs[0, 1], "Prediction Throughput (ins/pred-cycle) - Higher is Better", "Throughput (ins/pred)", throughputs, "{:.2f}", reverse_sort=True)

    # 3. CPI Penalty (Lower is better)
    plot_metric(axs[0, 2], "CPI Penalty (Cycles Lost to Mispredicts) - Lower is Better", "CPI Penalty", cpis, "{:.3f}", reverse_sort=False)

    # Row 2: Accuracy and Efficiency
    # 4. MPI (Mispredictions Per Instruction - Lower is better)
    plot_metric(axs[1, 0], "MPI (Mispredictions Per Instruction) - Lower is Better", "MPI", mpis, "{:.4f}", reverse_sort=False)

    # 5. EPI (Dynamic Energy Per Instruction - Lower is better)
    plot_metric(axs[1, 1], "EPI (Dynamic Energy Per Instruction) - Lower is Better", "EPI (pJ / Instruction)", epis, "{:.1f}", reverse_sort=False)

    # 6. DPI (Divergences Per Instruction - Lower is better)
    plot_metric(axs[1, 2], "DPI (Divergences Per Instruction) - Lower is Better", "DPI", dpis, "{:.4f}", reverse_sort=False)

    plt.tight_layout()
    output_path = Path(args.output)
    output_path.parent.mkdir(parents=True, exist_ok=True)
    plt.savefig(output_path, dpi=150, bbox_inches='tight')
    print(f"Saved plots to {output_path}")
    return 0

if __name__ == "__main__":
    sys.exit(main())
